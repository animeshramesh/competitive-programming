class Dubs {
    public:
    
    bool isDub(ll num) {
        string s = to_string(num);
        if (s[sz(s)-1] == s[sz(s)-2]) return true;
        return false; 
    }

    long long count(long long L, long long R) {

        ll c = 0;
        ll Rmin = (R/100) * 100;
        ll Lmax = ((L/100) * 100 ) + 100;

        c = ((Rmin - Lmax)/100) * 10;

        for (ll i = L; i <= Lmax; i++) 
        {
            if (isDub(i)) c++;
        }

        for (ll i = Rmin + 1; i <= R; i++){
            if (isDub(i)) c++;
        }
        
        return c;
    }
};

